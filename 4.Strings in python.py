#Strings in python
"""
1.One important thing to remember is that
we cannot print single quotes within a
single quote sentence(the same applies to double quotes as well)
2.However we can use single quotes or double quotes both as they will make no
difference at the time of printing
3.Single quotes can be used inside double quotes and vice-vera
"""

print("he is a 'good' developer")      #single quote within double quote

print("he is a \"good\" developer")   #double quote within double quote

print('he is a "good" developer')      #double quote within single quote

print('he is a \'good\' developer')   #single quote within single quote

