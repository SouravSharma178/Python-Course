#Mathematical operators in python
print(2**3)    #in python we use ** to print the exponent of a number

print(49**(1/2)) # this can be used to print the square root of a number

print(98/7)    #single slash is used to print float value

print(98//7)   #double slash is used to print integer value

print(7/3)

print(7%3)    #the mod operator is used to print the remainder

print(3984%2)

print(3983%2)

print(782.67%43)

print(564.09**2.3)

print(7%(5//2))