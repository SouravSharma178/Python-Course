# Tuples in python,they use square brackets

# Tuples are similar to lists except the fact that their values cannot be changed

# Tuples are immutable


fruits = ("Apple", "Mango", "Peach")
print(fruits[0])

# fruits[0] = "Pineapple" this is not possible as tuples are immutable


# tuples can also be declared without brackets

fruits2 = "Grapes", "Strawberry", "Banana"
print(fruits2)
